// Helper functions to extract data
export const getStatesNames = () => {
  return INDIA_MARKETS_DATA.states.map((state) => state.state_name);
};

export const getTalukasByState = (stateName) => {
  const state = INDIA_MARKETS_DATA.states.find(
    (s) => s.state_name === stateName
  );
  if (!state) return [];

  const allTalukas = [];
  state.districts.forEach((district) => {
    district.talukas.forEach((taluka) => {
      allTalukas.push(taluka.taluka_name);
    });
  });

  return [...new Set(allTalukas)]; // Remove duplicates
};

export const getMarketsByStateAndTaluka = (stateName, talukaName) => {
  const state = INDIA_MARKETS_DATA.states.find(
    (s) => s.state_name === stateName
  );
  if (!state) return [];

  const allMarkets = [];
  state.districts.forEach((district) => {
    const taluka = district.talukas.find((t) => t.taluka_name === talukaName);
    if (taluka) {
      allMarkets.push(...taluka.markets);
    }
  });

  return allMarkets;
};
